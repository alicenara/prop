    private void botoModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoModificarActionPerformed
        int x = (Integer) spinX.getValue();
        int y = (Integer) spinY.getValue();
        int estants = (Integer) spinEstants.getValue();
        int llargada = (Integer) spinLlargada.getValue();
        try {
           ArrayList<ArrayList<String> > estanteries = CtrlInterficie.seleccionaAllEstanteries();
           int s = comboEstanteria.getSelectedIndex();
           int xo = Integer.parseInt(estanteries.get(s).get(0));
           int yo = Integer.parseInt(estanteries.get(s).get(1));
           CtrlInterficie.modificarLlargadaEstanteria(xo, yo, llargada);
           CtrlInterficie.modificarNumFilesEstanteria(xo, yo, estants);
           if(xo != x || yo != y)CtrlInterficie.modificarCoordenadesEstanteria(xo, yo, x, y);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Error al modificar.\nCodi d'error: " + e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);  
            return;
        }
        JOptionPane.showMessageDialog(null, "Modificat correctament","Info",JOptionPane.INFORMATION_MESSAGE);
        resetFields();
    }//GEN-LAST:event_botoModificarActionPerformed

